package rgbvsu.engine.objects;

import java.awt.Color;
import java.awt.Graphics2D;
import java.awt.Point;
import rgbvsu.engine.World;
import rgbvsu.engine.ai.Enemy;
import rgbvsu.engine.util.Sprite;

/**
 *
 * @author Dauid
 */
public class PatrolDroid extends Enemy{

    
    public PatrolDroid(World world)
    {
        super(world,50,50);
        setPushable(true);
    }
    
    @Override
    public Sprite getSprite()
    {
        return null;
    }
    
    @Override
    public void update(long time) {
        //throw new UnsupportedOperationException("Not supported yet.");
    }

    @Override
    public void draw(int x, int y, Graphics2D g) {
        g.setColor(Color.WHITE);
        g.fillOval(x+getDrawX(),y+getDrawY(),50,50);
    }

    @Override
    public void checkCollision(GameObject otherobj) {
        if(otherobj instanceof Player)
        {
            Point facingOffset = getFacingOffset();
            Player player = (Player)otherobj;
            player.damage(5, facingOffset.x*player.getWidth(),facingOffset.y*player.getHeight(), 300);
        }
    }
    
}
